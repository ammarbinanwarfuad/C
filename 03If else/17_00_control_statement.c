/*Control statements are essential building blocks of C programming, enabling programmers to modify the program's execution flow based on specific conditions or repetitive actions. They are categorized into three main types:

    01.Selection Statements: These statements allow the program to make decisions based on certain conditions. The most common selection statements are:
        a.if-else statement: Executes a block of code if a condition is true, otherwise executes an optional else block.
        b.switch statement: Evaluates a variable against multiple cases and executes the corresponding code block for the matching case.

    02.Iteration Statements: These statements cause a block of code to be executed repeatedly until a specified condition is met. The most common iteration statements are:

        a.for loop: Executes a block of code a fixed number of times, controlled by a loop counter variable.
        b.while loop: Executes a block of code repeatedly as long as a specified condition remains true.
        c.do-while loop: Executes a block of code at least once, then checks a condition to determine if the loop should continue.

    03.Jump Statements: These statements abruptly change the program's execution flow, transferring control to a specific location in the code. The most common jump statements are:

        a.goto statement: Transfers control to a labeled statement within the program.
        b.break statement: Terminates the current loop and exits it immediately.
        c.continue statement: Skips the remaining iterations of the current loop and proceeds to the next iteration.

Control statements play a crucial role in structuring C programs, allowing programmers to implement complex logic, handle various scenarios, and create efficient algorithms. They form the foundation of decision-making and repetitive tasks in C programming.*/

